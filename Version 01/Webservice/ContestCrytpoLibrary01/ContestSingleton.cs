		/// <param name="numberOfExamples"></param>
		/// <returns></returns>
		public List<Example> generateOracleExampleList(int numberOfExamples)
		{
			List<Example> examplesToReturn = new List<Example>();

			Random r = new Random();
			int randomPrimePosition = 0;

			for (int i=0;i< numberOfExamples - 1; i++)
			{
				//Generate new example (true)
				
				randomPrimePosition = r.Next(0,totalAmountOfPrimes-1);
				examplesToReturn.Add(new Example(randomPrimePosition, primeNumbers[randomPrimePosition], true));

				//Generate new example (true)

				randomPrimePosition = r.Next(0, totalAmountOfPrimes - 1);
				examplesToReturn.Add(new Example(randomPrimePosition, primeNumbers[randomPrimePosition], true));

				//Generate new example (false)

				randomPrimePosition = r.Next(0, totalAmountOfPrimes - 1);

				if (i % 3 == 0)
				{
					examplesToReturn.Add(new Example(randomPrimePosition, 3 * primeNumbers[randomPrimePosition], false));
				}
				else if (i % 3 == 1)
				{
					examplesToReturn.Add(new Example(randomPrimePosition, 5 * primeNumbers[randomPrimePosition], false));
				}
				else
				{
					examplesToReturn.Add(new Example(randomPrimePosition, 7 * primeNumbers[randomPrimePosition], false));
				}

			}

			return examplesToReturn;
		}


